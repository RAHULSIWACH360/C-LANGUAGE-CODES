// Program for performing bitwise operations.
#include<stdio.h>

    int main(){
    
    int a = 5, b = 8;
    
    

    printf("\na and b %d %d\n",a , b);
    printf("a & b %d\n",a & b);
    printf("a | b %d\n",a | b);
    printf(" ~ b %d\n", ~ b);
    printf("a ^ b %d\n",a ^ b);
    printf("a << b %d\n",a << b);
    printf("a >> b %d\n",a >> b);

return 0;
}